// Emoji usage tracking
const STORAGE_KEY = 'chatter_emoji_usage';

const EmojiPicker = ({ onEmojiSelect, onClose, className = '' }) => {
  const [activeCategory, setActiveCategory] = useState('recent');
  const [searchQuery, setSearchQuery] = useState('');
  const searchInputRef = useRef(null);
  const pickerRef = useRef(null);

  // Use the enhanced emoji hook
  const { 
    recentEmojis, 
    frequentEmojis, 
    saveEmojiUsage, 
    searchEmojis,
    getFrequentEmojis 
  } = useEmojis();

  // Focus search input when picker opens
  useEffect(() => {
    if (searchInputRef.current) {
      searchInputRef.current.focus();
    }
  }, []);

  // saveEmojiUsage is now provided by the useEmojis hook

  // Handle emoji selection
  const handleEmojiClick = (emoji) => {
    saveEmojiUsage(emoji);
    onEmojiSelect?.(emoji);
    onClose?.();
  };

  // Filter emojis based on search query
  const filteredEmojis = useMemo(() => {
    if (!searchQuery.trim()) {
      if (activeCategory === 'recent') {
        return recentEmojis.length > 0 ? recentEmojis : EMOJI_DATA.smileys.slice(0, 32);
      }
      return EMOJI_DATA[activeCategory] || [];
    }

    // Use enhanced search from the hook
    return searchEmojis(searchQuery);
  }, [searchQuery, activeCategory, recentEmojis, searchEmojis]);

  // Handle keyboard navigation
  const handleKeyDown = (e) => {
    if (e.key === 'Escape') {
      onClose?.();
    }
  };

  return (
    <div 
      ref={pickerRef}
      className={`emoji-picker bg-white border border-gray-200 rounded-lg shadow-xl ${className}`}
      onKeyDown={handleKeyDown}
    >
      {/* Header */}
      <div className="flex items-center justify-between p-3 border-b border-gray-200">
        <h3 className="text-sm font-medium text-gray-900">Emoji</h3>
        <button
          onClick={onClose}
          className="p-1 text-gray-400 hover:text-gray-600 rounded"
          title="Close"
        >
          <X className="h-4 w-4" />
        </button>
      </div>

      {/* Search */}
      <div className="p-3 border-b border-gray-200">
        <div className="relative">
          <Search className="absolute left-3 top-1/2 transform -translate-y-1/2 h-4 w-4 text-gray-400" />
          <input
            ref={searchInputRef}
            type="text"
            placeholder="Search emojis..."
            value={searchQuery}
            onChange={(e) => setSearchQuery(e.target.value)}
            className="search-input w-full pl-9 pr-3 py-2 text-sm border border-gray-200 rounded-md focus:outline-none"
          />
        </div>
      </div>

      {/* Categories */}
      {!searchQuery && (
        <div className="flex items-center justify-center px-3 py-2 border-b border-gray-200">
          {CATEGORIES.map((category) => {
            const Icon = category.icon;
            return (
              <button
                key={category.id}
                onClick={() => setActiveCategory(category.id)}
                className={`category-button flex-shrink-0 p-2 mx-1 rounded-md ${
                  activeCategory === category.id
                    ? 'active'
                    : 'text-gray-500'
                }`}
                title={category.name}
              >
                <Icon className="h-4 w-4" />
              </button>
            );
          })}
        </div>
      )}

      {/* Main Content Area with Emoji Grid */}
      <div className="emoji-picker-content">
        {filteredEmojis.length > 0 ? (
          <div className="emoji-grid">
            {filteredEmojis.map((emoji, index) => (
              <button
                key={`${emoji}-${index}`}
                onClick={() => handleEmojiClick(emoji)}
                className="p-2 text-lg hover:bg-gray-100 rounded transition-colors focus:outline-none focus:ring-2 focus:ring-indigo-500 aspect-square flex items-center justify-center"
                title={emoji}
              >
                {emoji}
              </button>
            ))}
          </div>
        ) : (
          <div className="text-center py-8 text-gray-500">
            <Smile className="h-8 w-8 mx-auto mb-2 text-gray-300" />
            <p className="text-sm">
              {searchQuery ? 'No emojis found' : 'No recent emojis'}
            </p>
            {searchQuery && (
              <p className="text-xs mt-1">Try a different search term</p>
            )}
          </div>
        )}
      </div>




    </div>
  );
};

export default EmojiPicker; 